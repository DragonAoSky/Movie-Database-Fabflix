public class CheckStar {
    private String name;
    private String boa;
    private String id;

    public CheckStar() {
        this.name = null;
        this.boa = null;
        this.id = null;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBoa() {
        return boa;
    }

    public void setBoa(String boa) {
        this.boa = boa;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
